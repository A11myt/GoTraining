func TestBspAufg(t *testing.T) {
	// Beispiele aus der Aufgabenstellung
	tests := [...]struct {
		c     string
		v     string
		want1 int
		want2 int
	}{
		{"ABCA", "AEFC", 1, 1},
		{"ABCA", "AABB", 1, 2},
		{"ABCA", "BACB", 1, 2},
	}
	for _, tt := range tests {
		var c, v [anzStellen]string
		for i := 0; i < anzStellen; i++ {
			c[i] = tt.c[i : i+1]
			v[i] = tt.v[i : i+1]
		}
		got1, got2 := bewertung(c, v)
		if got1 != tt.want1 || got2 != tt.want2 {
			t.Errorf("bewertung(%v,%v) = (%v,%v), want (%v,%v)", c, v, got1, got2, tt.want1, tt.want2)
		}
	}
}
